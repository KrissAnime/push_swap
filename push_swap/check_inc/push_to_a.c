#include "../header/push_swap.h"

void	push_to_a(t_s **s)
{
	size_t	i;

	if ((*s)->ssb == 0)
		return ;
	i = 0;
	(*s)->ssa++;
	(*s)->ssb--;
	if ((*s)->ssa == 1)
		(*s)->sa[0] = (*s)->sb[0];
	if ((*s)->ssa >= 2)
	{
		while (i < (*s)->ssa)
		{
			(*s)->sa[(*s)->ssa - i] = (*s)->sa[(*s)->ssa - i - 1];
			i++;
		}
		(*s)->sa[0] = (*s)->sb[0];
	}
	i = 0;
	while (i < (*s)->ssb)
	{
		(*s)->sb[i] = (*s)->sb[i + 1];
		i++;
	}
}
