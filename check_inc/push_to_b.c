#include "../header/push_swap.h"

void	push_to_b(t_s **s)
{
	size_t	i;

	if ((*s)->ssa == 0)
		return ;
	i = 0;
	(*s)->ssa--;
	(*s)->ssb++;
	if ((*s)->ssa == 1)
		(*s)->sb[0] = (*s)->sa[0];
	if ((*s)->ssb >= 2)
	{
		while (i < (*s)->ssb)
		{
			(*s)->sb[(*s)->ssb - i] = (*s)->sa[(*s)->ssb - i - 1];
			i++;
		}
		(*s)->sb[0] = (*s)->sa[0];
	}
	i = 0;
	while (i < (*s)->ssa)
	{
		(*s)->sa[i] = (*s)->sa[i + 1];
		i++;
	}
}
