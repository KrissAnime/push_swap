#include "../header/push_swap.h"

void	swap_a(t_s **s)
{
	int	temp;

	if ((*s)->ssa > 1)
	{
		temp = (*s)->sa[0];
		(*s)->sa[0] = (*s)->sa[1];
		(*s)->sa[1] = temp;
	}
}
