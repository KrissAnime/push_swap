#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t	i;
	t_byte	*tmps1;
	t_byte	*tmps2;

	tmps1 = (t_byte*)s1;
	tmps2 = (t_byte*)s2;
	i = 0;
	while (i < n)
	{
		if (tmps1[i] != tmps2[i])
			return (tmps1[i] - tmps2[i]);
		i++;
	}
	return (0);
}
