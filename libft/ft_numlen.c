#include "libft.h"

size_t	ft_numlen(int num)
{
	size_t	x;

	x = 0;
	if (num == -2147483648)
		return (10);
	if (num < 0)
		num *= -1;
	if (num >= 0 && num < 10)
		return (1);
	while (num >= 1)
	{
		x++;
		num /= 10;
	}
	return (x);
}
